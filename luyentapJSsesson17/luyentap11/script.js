// Sử dụng hàm Math.abs()
let a=Math.abs(-5.5);//Tính giá trị tuyệt đối của -5.5, kết quả là 5.5
let b=Math.abs(10);//Tính giá trị tuyệt đối của 10, kết quả là 10

// Sử dụng hàm Math.ceil()
let c=Math.ceil(4.4);// Làm tròn số 4.4 lên số nguyên gần nhất, kết quả là 5  
let d=Math.ceil(5.8);  // Làm tròn số 5.8 lên số nguyên gần nhất, kết quả là 6  

// Sử dụng hàm Math.floor()
let e=Math.floor(5.7); // Làm tròn số 5.7 xuống số nguyên gần nhất, kết quả là 5
let f=Math.floor(6.1); // Làm tròn số 6.1 xuống số nguyên gần nhất, kết quả là 6

// Sử dụng hàm Math.round()
let g=Math.round(10.1);// Làm tròn số 10.1 đến số nguyên gần nhất, kết quả là 10
let h=Math.round(20.6);// Làm tròn số 20.6 đến số nguyên gần nhất, kết quả là 21







 